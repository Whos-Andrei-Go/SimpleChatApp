    private void startServer() {
        new Thread(() -> {
            try {
                serverSocket = new ServerSocket(5432);
                appendMessage("Server started. Waiting for clients...");
                clientSocket = serverSocket.accept();
                appendMessage("Client connected: " + clientSocket.getInetAddress());

                BufferedReader br = new BufferedReader(new InputStreamReader(clientSocket.getInputStream()));
                pw = new PrintWriter(clientSocket.getOutputStream(), true);

                appendMessage("Connected to client. Starting chat...");
                pw.println("Welcome to the chat!");

                new Thread(() -> receiveMessages(br)).start();

                inputField.addActionListener(e -> {
                    String message = inputField.getText();
                    inputField.setText("");
                    pw.println(message);
                    appendMessage("You: " + message);
                    if (message.equalsIgnoreCase("exit")) {
                        cleanupResources();
                        System.exit(0);
                    }
                });

            } catch (IOException e) {
                appendMessage("Error starting server: " + e.getMessage());
            }
        }).start();
    }
    
    private void receiveMessages(BufferedReader br) {
        try {
            String clientMessage;
            while ((clientMessage = br.readLine()) != null) {
                appendMessage("Client: " + clientMessage);
            }
        } catch (IOException e) {
            appendMessage("Client disconnected.");
        }
    }

    private void appendMessage(String message) {
        SwingUtilities.invokeLater(() -> chatArea.append(message + "\n"));
    }
    
    private void cleanupResources() {
        try {
            if (clientSocket != null) clientSocket.close();
            if (serverSocket != null) serverSocket.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

